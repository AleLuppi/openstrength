<template>
  <div class="q-pa-md">
    <q-form @submit="onSubmit" class="q-my-md q-gutter-sm column">
      <h3 class="text-primary">{{ $t("user.auth.signup_title") }}</h3>

      <q-input
        v-model="name"
        outlined
        type="text"
        label="Full name"
        :rules="[
          (val) => (val && val.length > 2) || $t('user.auth.name_required'),
        ]"
        lazy-rules
      />

      <q-input
        ref="emailInput"
        v-model="email"
        outlined
        type="email"
        label="Email"
        :rules="[
          (val) => (val && val.length > 2) || $t('user.auth.email_required'),
          (val) => validateEmail(val) || $t('user.auth.email_invalid'),
        ]"
        lazy-rules
        :error="emailError"
        :error-message="emailErrorMessage"
      />

      <q-input
        ref="passwordInput"
        v-model="password"
        outlined
        :type="passwordVisible ? 'text' : 'password'"
        label="Password"
        :rules="[
          (val) =>
            (val && validatePassword(val)) || $t('user.auth.password_invalid'),
        ]"
        lazy-rules
        :error="passwordError"
        :error-message="passwordErrorMessage"
      >
        <template v-slot:append>
          <q-icon
            :name="passwordVisible ? 'visibility' : 'visibility_off'"
            class="cursor-pointer"
            @click="passwordVisible = !passwordVisible"
          />
        </template>
      </q-input>

      <div class="row">
        <q-toggle
          v-model="accept"
          checked-icon="check"
          color="green"
          unchecked-icon="clear"
        />

        <p class="col" style="min-width: 8em">
          {{ $t("user.auth.acceptance_before")
          }}<router-link :to="{ name: 'privacy_policy' }" @click.stop="">{{
            $t("layout.views.privacy_policy")
          }}</router-link
          >{{ $t("user.auth.acceptance_between")
          }}<router-link :to="{ name: 'terms_conditions' }" @click.stop="">{{
            $t("layout.views.terms_conditions")
          }}</router-link
          >{{ $t("user.auth.acceptance_after") }}
        </p>
      </div>

      <q-btn :label="$t('user.auth.register_button')" type="submit" />
    </q-form>

    <router-link :to="{ name: 'login' }">{{
      $t("user.auth.signup_to_signin")
    }}</router-link>
  </div>
</template>

<script setup lang="ts">
import { ref, watch } from "vue";
import { useQuasar, QInput } from "quasar";
import { useI18n } from "vue-i18n";
import { User } from "firebase/auth";
import {
  AuthError,
  doCreateUserWithEmailAndPassword,
} from "@/helpers/users/auth";
import { validateEmail, validatePassword } from "@/helpers/users/validate";

// Init plugin
const $q = useQuasar();
const i18n = useI18n();

const emailInput = ref<QInput>();
const passwordInput = ref<QInput>();
const name = ref("");
const email = ref("");
const emailError = ref(false);
const emailErrorMessage = ref("");
const password = ref("");
const passwordError = ref(false);
const passwordErrorMessage = ref("");
const passwordVisible = ref(false);
const accept = ref(false);

// Clear external errors on user typing
watch(email, () => {
  emailError.value = false;
  emailErrorMessage.value = "";
});
watch(password, () => {
  passwordError.value = false;
  passwordErrorMessage.value = "";
});

/**
 * Submit form according to inputs' values
 */
function onSubmit() {
  if (accept.value == true) {
    doCreateUserWithEmailAndPassword(
      email.value ?? "",
      password.value ?? "",
      true,
      onSubmitSuccess,
      onSubmitFailure,
    );
  } else {
    $q.notify({
      type: "negative",
      message: i18n.t("user.auth.acceptance_required"),
      position: "bottom",
    });
  }
}

/**
 * Manage successful form submit
 */
function onSubmitSuccess(user: User) {
  // TODO
  console.log(user.uid);
}

/**
 * Manage faulty form submit
 *
 * @param authError error that caused form submit failure
 */
function onSubmitFailure(authError: AuthError) {
  switch (authError) {
    case AuthError.userError:
      emailError.value = true;
      emailErrorMessage.value = i18n.t("user.auth.user_already_exist");
      emailInput.value?.focus();
      break;
    case AuthError.emailError:
      emailError.value = true;
      emailErrorMessage.value = i18n.t("user.auth.email_invalid");
      emailInput.value?.focus();
      break;
    case AuthError.passwordError:
      passwordError.value = true;
      passwordErrorMessage.value = i18n.t("user.auth.password_invalid");
      passwordInput.value?.focus();
      break;
    default:
      $q.notify({
        type: "negative",
        message: i18n.t("user.auth.signup_error"),
        position: "bottom",
      });
      break;
  }
}
</script>
